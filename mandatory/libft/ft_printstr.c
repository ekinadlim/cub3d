#include "libft.h"

void	ft_printstr(char *s, int *count)
{
	if (s == 0)
	{
		write(1, "(null)", 6);
		(*count) += 6;
	}
	else
	{
		write(1, s, ft_strlen(s));
		(*count) += ft_strlen(s);
	}
}
